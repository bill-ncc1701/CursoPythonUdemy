"""
Python = Linguagem de Programação
Tipo de tipagem = Dinâmica / Fortemente tipada
str -> string -> textos
Strings são textos entre aspas simples ou duplas.
"""

# Aspas simples
print('Olá, Willian')

# Aspas duplas
print("Olá, Otávio!")

# Ecape \
print('\'Olá, Willian\'')
print("Olá, Willian \"Otávio\"")

# r
print(r'\'Olá, Willian\'')

